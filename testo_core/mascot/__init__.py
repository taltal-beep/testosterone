def _smash_arm(canvas: Frame, hand_y: int) -> Frame:
    """Arm reaching in from the top edge with the hand at hand_y."""
    for y in range(hand_y):
        canvas = _paste(canvas, ["oWwWo"], 8, y)
    return _paste(canvas, _SMASH_HAND, 7, hand_y)


