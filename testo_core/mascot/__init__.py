"""Testo pixel mascot animations.

Every animation is a list of frames; a frame is a list of equal-length
strings where each character indexes PALETTE and "." is transparent.
The same grids drive the React mascot components (see
frontend/src/components/mascot/animations.ts, generated by export_ts.py)
and the terminal player (testo_core.mascot.render).

Play in a terminal:  python -m testo_core.mascot flex
"""

from __future__ import annotations

Frame = list[str]
Animation = list[Frame]

# Char -> hex color. Shared by web and CLI renderers.
PALETTE: dict[str, str] = {
    "o": "#232733",  # outline
    "s": "#f2b380",  # skin
    "d": "#cd8756",  # skin shade
    "h": "#ffd9ae",  # skin highlight
    "w": "#3b82f6",  # wristband (brand blue)
    "W": "#1d4ed8",  # wristband shade
    "r": "#ef4444",  # red glove
    "R": "#a61b1b",  # red glove shade
    "u": "#3b82f6",  # blue glove
    "U": "#1e40af",  # blue glove shade
    "b": "#c46a1f",  # brick
    "B": "#8a4210",  # brick shade
    "y": "#ffd23e",  # impact spark
    "x": "#ffffff",  # flash / glove shine
    "t": "#7dd3fc",  # sweat drop
    "p": "#e9e9ef",  # bandage
    "g": "#8b93a3",  # debris / speed lines
}


def _blank(width: int, height: int) -> Frame:
    return ["." * width for _ in range(height)]


def _paste(canvas: Frame, sprite: Frame, x: int, y: int) -> Frame:
    """Overlay sprite onto canvas at (x, y); '.' pixels stay transparent."""
    out = list(canvas)
    for sy, srow in enumerate(sprite):
        cy = y + sy
        if not 0 <= cy < len(out):
            continue
        row = list(out[cy])
        for sx, ch in enumerate(srow):
            cx = x + sx
            if ch != "." and 0 <= cx < len(row):
                row[cx] = ch
        out[cy] = "".join(row)
    return out


def _mirror(sprite: Frame, swap: dict[str, str]) -> Frame:
    table = str.maketrans(swap)
    return [row[::-1].translate(table) for row in sprite]


